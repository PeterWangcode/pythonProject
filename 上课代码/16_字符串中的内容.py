# 统计字符串中的大、小字母、数字、空格各有多少个

s = input('请输入包含大、小写字母、数字、空格的字符串：')
# 小写字母、大写字母、数字、空格
X, D, S, K = 0, 0, 0, 0

for i in s:
    if 'a' <= i <= 'z':
        X += 1
    elif 'A' <= i <= 'Z':
        D += 1
    elif '0' <= i <= '9':
        S += 1
    elif i == ' ':
        K += 1
print(f'小写字母：{X}个\n大写字母：{D}个\n数字：{S}个\n空格：{K}个')
