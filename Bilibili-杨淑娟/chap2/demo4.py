# 数据类型
# 常用数据类型
# （1）整数类型    ---> int --->例：10
# （2）浮点数类型  ---> float --->例：10.5
# （3）布尔类型    ---> bool --->例：True,False
# （4）字符串类型   ---> str  --->例:'人生苦短，我学python'


# 整数类型
# 可以表示：正数、负数、0
n1 = 100
n2 = -100
n3 = 0
print(n1, type(n1))
print(n2, type(n2))
print(n3, type(n3))
# 整数可以表示为二进制，十进制，八进制，十六进制
print('十进制', 100)  # 默认为十进制
print('二进制', 0b10101111)  # 若要显示 二进制数 需要在前面以’0b‘开头
print('八进制', 0o176)  # 若要显示 八进制数 需要在前面以'0o'开头
print('十六进制', 0x1EAF)  # 若要显示 十六进制数 需要在前面以’0x’开头
