# 字符串类型
# 字符串又被称为不可变的字符序列
# 可以使用'单引号‘，“双引号”，'''三引号'''或“”“三引号”“” 来定义
# 单引号和双引号定义的字符串必须在一行
# 三引号定义的字符串可以分布在连续的多行
str1 = '人生苦短，我用python'
str2 = "人生苦短，我用python"

str3 = '''人生苦短，
    我用python'''

str4 = """人生苦短，
    我用python"""

print(str1, type(str1))
print(str2, type(str2))
print(str3, type(str3))
print(str4, type(str4))
